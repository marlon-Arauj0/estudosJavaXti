public class Operador {

	public static void main (String[] args){
		//2 + 3 binário
		//-2 unário
		//true ? "sim" : "não" operador ternário
		
		int a = 9 + 4;
		System.out.println(a);
		
		String b = "9" + "4";
		System.out.println(b);
		
		double c = 7 - 4 + 3 * 2;
		System.out.println(c);
		
		double d = (7 - 4 + 3) * 2;
		System.out.println(d); 
	}
}
